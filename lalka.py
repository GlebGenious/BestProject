width = int(input("Enter the number of columns: "))
matrix = []
for i in range(length):
    row = []
    for j in range(width):
        sample = f"{i + 1} x {j + 1}"
        result = (i+1) * (j+1)
        row.append(f"{result:4d} ({sample})")
    matrix.append(row)
for row in matrix:
    print("\t".join(row))




